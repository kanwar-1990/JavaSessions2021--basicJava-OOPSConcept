package ImpJavaSesssion;

public class TypeCasting {

	public static void main(String args[]) {

		int i;
		// Converting double value into int means Type Casting (converting a particular
		// type data into another)
		i = (int) 20.00;
		double a = i;

		System.out.println(i);
		System.out.println(a);
		// Type(datatype) Casting

		// • Converting one type of primitive datatype into another type of
		// primitive datatype is called as type casting.

	}

}
